package CursoEmVideoPOO.JavaLessons;

public class ArithmeticOperators {
    /*
        ---OPERADORES---

    Pi -> constante -->  "Math.PI = 3,141592..."
    pow -> exponenciação --> "Math.pow(5,2) = 25"
    sqrt -> raiz quadrada --> "Math.sqrt(25) = 5"
    cbrt -> raiz cúbica --> "Math.cbrt(27) = 3"

        ---ARREDONDAMENTO---

    abs -> valor absoluto --> "Math.abs(-10) = 10"
    floor -> arredonda para baixo --> "Math.floor(3.9) = 3"
    ceil -> arredonda para cima --> "Math.floor(3.6) = 4"
    round -> arredonda aritmeticamente --> "Math.round(7.7) = 8" || "Math.round(7.3) = 7"

        --------------------
    "Math.random()" --> gera um número entre 0 e 1
    (5 + Math.random() * (10-5)) --> para gerar um número entre 5 e 10

     */
}
